/**
 * Get theme customization for a given OS/distro
 */
export function getDistroTheme(osFamily: OSFamily, distro?: string | null): DistroTheme {
  const defaultTheme: DistroTheme = {
    accentColor: '#ff8c42', // Caro orange
    secondaryColor: '#ea4aaa', // Caro pink
    terminalBg: '#1e1e2e',
    terminalText: '#cdd6f4',
    promptStyle: 'default',
    cssClass: 'distro-default',
  };

  if (osFamily === 'macos') {
    return {
      ...defaultTheme,
      accentColor: '#0071e3',
      secondaryColor: '#333333',
      cssClass: 'distro-macos',
    };
  }

  if (osFamily === 'windows') {
    return {
      ...defaultTheme,
      accentColor: '#0078D4',
      secondaryColor: '#50e6ff',
      promptStyle: 'powershell',
      cssClass: 'distro-windows',
    };
  }

  // Linux distro-specific themes
  if (distro && LINUX_DISTROS[distro as LinuxDistro]) {
    const info = LINUX_DISTROS[distro as LinuxDistro];
    return {
      accentColor: info.color,
      secondaryColor: defaultTheme.secondaryColor,
      terminalBg: '#1e1e2e',
      terminalText: '#cdd6f4',
      promptStyle: 'bash',
      cssClass: `distro-${distro.replace(/-/g, '_')}`,
    };
  }

  return defaultTheme;
}

